#include <stdio.h>
int main()
{
    // check whether a given number is a three-digit number or not.

    int num, length;
    printf("Enter a number : ");
    scanf("%d", &num);

    if (length = printf("%d",num) == 3)
    {
        printf("Given number is a three-digit number.");
    }
    else 
    {
        printf("Given number is not a three-digit number.");
    }

    return 0;
}